// // historia de js
// /*
// JavaScript fue creado en 1995 por Brendan Eich en Netscape Communications Corporation.
// Originalmente se llamó LiveScript, pero se renombró a JavaScript para aprovechar la popularidad de Java.
// Fue diseñado para ser un lenguaje de scripting ligero para hacer las páginas web interactivas.
// En 1997, JavaScript fue estandarizado por ECMA International bajo el nombre de ECMAScript.
// Desde entonces, ha evolucionado significativamente, con nuevas versiones de ECMAScript lanzadas anualmente.
// Hoy en día, JavaScript es uno de los lenguajes de programación más populares y se utiliza no solo en el navegador,
// sino también en el lado del servidor (Node.js), aplicaciones móviles (React Native), y mucho más.
// */
// // Características de JavaScript
// /*
// - Lenguaje interpretado o Just-in-time compilado: No necesita un compilador tradicional, se ejecuta directamente.
// - Basado en prototipos: A diferencia de los lenguajes basados en clases, JavaScript usa prototipos para la herencia.
// - Dinámicamente tipado: Las variables no tienen un tipo fijo, pueden cambiar de tipo durante la ejecución.
// - Débilmente tipado: Permite conversiones de tipo implícitas, lo que a veces puede llevar a comportamientos inesperados.
// - Multiparadigma: Soporta programación orientada a objetos, programación funcional y programación imperativa.
// - Concurrencia basada en un modelo de eventos: Utiliza un "event loop" para manejar operaciones asíncronas sin bloquear el hilo principal.
// - Versátil: Se ejecuta en el navegador, en el servidor (Node.js), en aplicaciones de escritorio (Electron) y móviles (React Native).
// */

